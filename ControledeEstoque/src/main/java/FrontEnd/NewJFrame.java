    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Mensagem = "";
        int Cont = 0;
        boolean Continuar = true;

        if (Marca.getText().isEmpty() == true)
        {
            Mensagem += "\nMarca (Vazio)";
            Marca.requestFocus();
            Cont++;
        }
        if (Modelo.getText().isEmpty() == true)
        {
            Mensagem += "\nModelo (Vazio)";
            Modelo.requestFocus();
            Cont++;
        }
        if (Cor.getText().isEmpty() == true)
        {
            Mensagem += "\nCor (Vazio)";
            Cor.requestFocus();
            Cont++;
        }
        if (Gas.getText().isEmpty() == true)
        {
            Mensagem += "\nTipo de Gás (Vazio)";
            Gas.requestFocus();
            Cont++;
        }
        if (BTU.getText().isEmpty() == true)
        {
            Mensagem += "\nPotência de BTU (Vazio)";
            BTU.requestFocus();
            Cont++;
        }
        try
        {
            float Pot_BTU = Integer.parseInt(BTU.getText());
        }
        catch (NumberFormatException E)
        {
            Mensagem += "\nPotência de BTU (Insira um valor Inteiro)";
            Voltagem.requestFocus();
            Cont++;
        }
        if (Voltagem.getSelectedIndex() == -1)
        {
            Mensagem += "\nVoltagem (Vazio)";
            Voltagem.requestFocus();
            Cont++;
        }
        if (Integer.parseInt(String.valueOf(Garantia.getValue())) < 0)
        {
            Mensagem += "\nGarantia (Maior ou igual a 0)";
            Garantia.requestFocus();
            Cont++;
        }

        if (Cont > 1)
        {
            Mensagem = "Por Favor, Corrija os Campos de: " + Mensagem;
            Continuar = false;
            JOptionPane.showMessageDialog(this, Mensagem, "AVISO", 2);
        }
        else if (Cont == 1)
        {
            Mensagem = "Por Favor, Corrija o Campo de: " + Mensagem;
            Continuar = false;
            JOptionPane.showMessageDialog(this, Mensagem, "AVISO", 2);
        }

        if(Continuar)
        {

            String SQL = " INSERT INTO arcondicionado (Marca, Modelo, Cor, TipoGas, PotenciaBTU, TempoGarantia, Voltagem) " +
            " VALUES ( " +
            " '" + Marca.getText() + "', " +
            " '" + Modelo.getText() + "', " +
            " '" + Cor.getText() + "', " +
            " '" + Gas.getText() + "', " +
            " '" + BTU.getText() + "', " +
            " '" + Garantia.getValue() + "', " +
            " '" + Voltagem.getSelectedItem()+ "' " +
            " ) ";

            C.SQLExecute(SQL);

            MostrarDados();

            Ares.setModel(DTM);

            JOptionPane.showMessageDialog(this, "Ar Condicionado Cadastrado com Sucesso", "AVISO", 1);

            Marca.setText("");
            Modelo.setText("");
            Cor.setText("");
            Gas.setText("");
            BTU.setText("");
            Garantia.setValue(0);
            Voltagem.setSelectedIndex(-1);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (DTM.getRowCount()>0)
        {
            if (Ares.getSelectedRowCount() > 0)
            {
                if (JOptionPane.showConfirmDialog(this, "Deseja Deletar o Ar Condicionado "
                    + Ares.getValueAt(Ares.getSelectedRow(), 1)
                    + " Modelo " + Ares.getValueAt(Ares.getSelectedRow(), 2), "AVISO", 0) == 0)
            {
                String SQL;
                SQL = "DELETE FROM arcondicionado WHERE CodArCondicionado = "
                + (Ares.getValueAt(Ares.getSelectedRow(), 0));

                C.SQLExecute(SQL);

                JOptionPane.showMessageDialog(this, "Ar Condicionado Deletado com Sucesso");

                MostrarDados();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela para Deletar", "AVISO", 0);
        }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "A Tabela está Vazia, é impossível deletar algo", "AVISO", 0);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

