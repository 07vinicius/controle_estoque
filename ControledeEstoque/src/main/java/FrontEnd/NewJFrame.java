    void MostrarDados()
    {
        while (DTM.getRowCount() > 0)
        {
            DTM.removeRow(0);
        }
            
        try
        {
                C.setResultSet("Select * from arcondicionado");
                if (C.getResultSet().first() == true)
                {
                    do
                    {
                        DTM.addRow
                        (
                            new Object[]
                            {
                                C.getResultSet().getString("CodArCondicionado"),
                                C.getResultSet().getString("Marca"),
                                C.getResultSet().getString("Modelo"),
                                C.getResultSet().getString("Cor"),
                                C.getResultSet().getString("TipoGas"),
                                C.getResultSet().getString("PotenciaBTU"),
                                C.getResultSet().getString("TempoGarantia"),
                                C.getResultSet().getString("Voltagem")
                            }
                        
                        );
                    }
                    while (C.getResultSet().next() == true);
                }
        }
        catch (Exception E){}
    };
    
